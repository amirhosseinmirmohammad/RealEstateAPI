namespace RealEstateCore.Enums
{
    /// <summary>
    /// Enumeration representing the status of a real estate property.
    /// </summary>
    public enum RealEstateStatus
    {
        /// <summary>
        /// The property is active and available.
        /// </summary>
        Active,

        /// <summary>
        /// The property has been archived and is not active.
        /// </summary>
        Archived,

        /// <summary>
        /// The property listing has expired.
        /// </summary>
        Expired
    }
}